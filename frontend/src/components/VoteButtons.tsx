import { h } from 'preact'
import { useState } from 'preact/hooks'
import { voteArticle, getStoredUser } from '../lib/api'
import { route } from 'preact-router'

export default function VoteButtons({ article, onVote }) {
  const [rating, setRating] = useState(article.rating ?? 0)
  const [userVote, setUserVote] = useState(article.user_vote ?? 0)
  const [loading, setLoading] = useState(false)
  const user = getStoredUser()

  const handleVote = async (value) => {
    if (loading) return
    if (!user) {
      route('/login')
      return
    }
    setLoading(true)
    try {
      const data = await voteArticle(article.slug, value)
      setRating(data.rating)
      setUserVote(data.user_vote ?? 0)
      if (onVote) onVote(data)
    } catch (err) {
      console.error('Vote error:', err)
    }
    setLoading(false)
  }

  return (
    <div className="vote-buttons d-flex align-items-center gap-2">
      <button
        className={`vote-btn upvote ${userVote === 1 ? 'active' : ''}`}
        onClick={() => handleVote(1)}
        disabled={loading}
        title={user ? 'Голосовать за' : 'Войдите для голосования'}
      >
        <svg xmlns="http://www.w3.org/2000/svg" width="22" height="22" fill="currentColor" viewBox="0 0 16 16">
          <path d="M8.864.046C7.908-.193 7.02.53 6.956 1.466c-.072 1.051-.23 2.016-.428 2.59-.125.36-.479 1.013-1.04 1.639-.557.623-1.282 1.178-2.131 1.41C2.685 7.288 2 7.87 2 8.72v4.001c0 .845.682 1.464 1.448 1.545 1.07.114 1.564.415 2.068.723l.012.007c.519.317 1.018.62 2.17.62.72 0 1.302-.227 1.73-.573.41-.33.663-.773.79-1.175.126-.4.17-.82.17-1.22V9.57c0-.4.044-.82.17-1.22.127-.4.38-.844.79-1.174.428-.346 1.01-.573 1.73-.573.53 0 .99.13 1.36.36.37.23.63.57.77.96.14.39.16.82.06 1.24-.1.42-.33.8-.66 1.1-.33.3-.75.5-1.22.58-.47.08-.96.04-1.4-.12-.44-.16-.82-.44-1.08-.8-.26-.36-.4-.8-.4-1.26 0-.46.14-.9.4-1.26.26-.36.64-.64 1.08-.8.44-.16.93-.2 1.4-.12.47.08.89.28 1.22.58.33.3.56.68.66 1.1.1.42.08.85-.06 1.24-.14.39-.4.73-.77.96-.37.23-.83.36-1.36.36-.72 0-1.302-.227-1.73-.573-.41-.33-.663-.773-.79-1.175-.126-.4-.17-.82-.17-1.22V3.5c0-.4-.044-.82-.17-1.22-.127-.4-.38-.844-.79-1.174C9.666.76 9.084.533 8.364.533c-.53 0-.99.13-1.36.36-.37.23-.63.57-.77.96-.14.39-.16.85-.06 1.27.1.42.33.8.66 1.1.33.3.75.5 1.22.58.47.08.96.04 1.4-.12.44-.16.82-.44 1.08-.8.26-.36.4-.8.4-1.26 0-.46-.14-.9-.4-1.26-.26-.36-.64-.64-1.08-.8-.44-.16-.93-.2-1.4-.12-.47.08-.89.28-1.22.58-.33.3-.56.68-.66 1.1-.1.42-.08.85.06 1.24.14.39.4.73.77.96.37.23.83.36 1.36.36z"/>
        </svg>
      </button>
      <span className="fw-bold" style={{ minWidth: '2rem', textAlign: 'center' }}>{rating}</span>
      <button
        className={`vote-btn downvote ${userVote === -1 ? 'active' : ''}`}
        onClick={() => handleVote(-1)}
        disabled={loading}
        title={user ? 'Голосовать против' : 'Войдите для голосования'}
      >
        <svg xmlns="http://www.w3.org/2000/svg" width="22" height="22" fill="currentColor" viewBox="0 0 16 16" style={{ transform: 'rotate(180deg)' }}>
          <path d="M8.864.046C7.908-.193 7.02.53 6.956 1.466c-.072 1.051-.23 2.016-.428 2.59-.125.36-.479 1.013-1.04 1.639-.557.623-1.282 1.178-2.131 1.41C2.685 7.288 2 7.87 2 8.72v4.001c0 .845.682 1.464 1.448 1.545 1.07.114 1.564.415 2.068.723l.012.007c.519.317 1.018.62 2.17.62.72 0 1.302-.227 1.73-.573.41-.33.663-.773.79-1.175.126-.4.17-.82.17-1.22V9.57c0-.4.044-.82.17-1.22.127-.4.38-.844.79-1.174.428-.346 1.01-.573 1.73-.573.53 0 .99.13 1.36.36.37.23.63.57.77.96.14.39.16.82.06 1.24-.1.42-.33.8-.66 1.1-.33.3-.75.5-1.22.58-.47.08-.96.04-1.4-.12-.44-.16-.82-.44-1.08-.8-.26-.36-.4-.8-.4-1.26 0-.46.14-.9.4-1.26.26-.36.64-.64 1.08-.8.44-.16.93-.2 1.4-.12.47.08.89.28 1.22.58.33.3.56.68.66 1.1.1.42.08.85-.06 1.24-.14.39-.4.73-.77.96-.37.23-.83.36-1.36.36-.72 0-1.302-.227-1.73-.573-.41-.33-.663-.773-.79-1.175-.126-.4-.17-.82-.17-1.22V3.5c0-.4-.044-.82-.17-1.22-.127-.4-.38-.844-.79-1.174C9.666.76 9.084.533 8.364.533c-.53 0-.99.13-1.36.36-.37.23-.63.57-.77.96-.14.39-.16.85-.06 1.27.1.42.33.8.66 1.1.33.3.75.5 1.22.58.47.08.96.04 1.4-.12.44-.16.82-.44 1.08-.8.26-.36.4-.8.4-1.26 0-.46-.14-.9-.4-1.26-.26-.36-.64-.64-1.08-.8-.44-.16-.93-.2-1.4-.12-.47.08-.89.28-1.22.58-.33.3-.56.68-.66 1.1-.1.42-.08.85.06 1.24.14.39.4.73.77.96.37.23.83.36 1.36.36z"/>
        </svg>
      </button>
    </div>
  )
}
